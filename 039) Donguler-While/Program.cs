// While

// Ekrana 5 kere "Merhaba Dünya" yazdırınız.

int sayac = 0;
while (sayac < 5)
{
    Console.WriteLine("Merhaba Dünya");
    sayac++;
}
Console.WriteLine("-----------------------------------");
// Ekrana 1'den 10'a kadar olan sayıları yazdırınız.

int i = 1;
while (i <= 10)
{
    Console.WriteLine(i);
    i++;
}
Console.WriteLine("-----------------------------------");

